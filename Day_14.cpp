#include <cmath>
#include <cstdio>
#include <vector>
#include <iostream>
#include <algorithm>

using namespace std;

class Difference {
    private:
    vector<int> elements;
  
  	public:
  	int maximumDifference;

    //constuctor
	Difference(vector<int> a){ 
        this->elements=a;
    }

    void computeDifference() {
    auto minmax = std::minmax_element(elements.begin(), elements.end());
    maximumDifference = *minmax.second - *minmax.first;
}



}; // End of Difference class

int main() {
    int N;
    cin >> N;
    
    vector<int> a;
    
    for (int i = 0; i < N; i++) {
        int e;
        cin >> e;
        
        a.push_back(e);
    }
    
    Difference d(a);
    
    d.computeDifference();
    
    cout << d.maximumDifference;
    
    return 0;
}